import UIKit

extension UIFont {

    static func latoMedium(size: CGFloat = 14) -> UIFont {
        return UIFont(name: "Lato-Medium", size: size)!
    }

    static func latoRegular(size: CGFloat = 14) -> UIFont {
        return UIFont(name: "Lato-Regular", size: size)!
    }

    static func latoBlack(size: CGFloat = 14) -> UIFont {
        return UIFont(name: "Lato-Black", size: size)!
    }

    static func latoBold(size: CGFloat = 14) -> UIFont {
        return UIFont(name: "Lato-Bold", size: size)!
    }

    static func semiBold(size: CGFloat = 14) -> UIFont {
        return UIFont(name: "Lato-Semibold", size: size)!
    }

    static func concourse(size: CGFloat = 14) -> UIFont {
        return UIFont(name: "ConcourseC4", size: size)!
    }

    static func concourseBold(size: CGFloat = 14) -> UIFont {
        return UIFont(name: "Concourse C4 Bold", size: size)!
    }

}
